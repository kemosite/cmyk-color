		color_obj.cmyk2lab(color_obj.cmyk_cyan_input, color_obj.cmyk_magenta_input, color_obj.cmyk_yellow_input, color_obj.cmyk_black_input);

		// color_obj.cmy_cyan_input = $(".cmy-cyan-input")[0];
